function createTableData(arr,currentPage) {
    tbody.innerHTML = ""
    for(let i = (currentPage-1)*recordsPerPage; i < (currentPage*recordsPerPage); i++){
        tr = tbody.insertRow();
        if(arr[i]) {
            for(let heading of header) {
                let tablecell = tr.insertCell();
                tablecell.innerHTML = arr[i][heading];
            }
        }
    }
}



